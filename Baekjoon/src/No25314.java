import java.util.Scanner;

public class No25314 {

	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);

		int n = scan.nextInt();
		String s = "";
		
		if(n > 4) {
		for (int i = 8; i <= n; i+=4) {
			s += "long";
		}
	
	
	}
		s += "long int";
		System.out.println(s);
	
	
	
	
	}

}
